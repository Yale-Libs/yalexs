def test_session_response_prefers_access_token_header_over_august() -> None:
    common = _build_common()
    result = common._authentication_from_session_response(
        "iid",
        {HEADER_ACCESS_TOKEN: "new", HEADER_AUGUST_ACCESS_TOKEN: "old"},
        {
            "expiresAt": _fmt(datetime.now(timezone.utc) + timedelta(days=1)),
            "vPassword": True,
            "vInstallId": True,
        },
    )
    assert result.access_token == "new"
    assert result.state == AuthenticationState.AUTHENTICATED


def test_session_response_falls_back_to_august_header() -> None:
    common = _build_common()
    result = common._authentication_from_session_response(
        "iid",
        {HEADER_AUGUST_ACCESS_TOKEN: "old"},
        {
            "expiresAt": _fmt(datetime.now(timezone.utc) + timedelta(days=1)),
            "vPassword": True,
            "vInstallId": True,
        },
    )
    assert result.access_token == "old"


def test_session_response_bad_password() -> None:
    common = _build_common()
    result = common._authentication_from_session_response(
        "iid",
        {HEADER_AUGUST_ACCESS_TOKEN: "x"},
        {
            "expiresAt": _fmt(datetime.now(timezone.utc)),
            "vPassword": False,
            "vInstallId": True,
        },
    )
    assert result.state == AuthenticationState.BAD_PASSWORD


def test_session_response_requires_validation() -> None:
    common = _build_common()
    result = common._authentication_from_session_response(
        "iid",
        {HEADER_AUGUST_ACCESS_TOKEN: "x"},
        {
            "expiresAt": _fmt(datetime.now(timezone.utc)),
            "vPassword": True,
            "vInstallId": False,
        },
    )
    assert result.state == AuthenticationState.REQUIRES_VALIDATION


def test_should_refresh_false_when_not_authenticated() -> None:
    common = _build_common()
    common._authentication.state = AuthenticationState.REQUIRES_AUTHENTICATION
    assert not common.should_refresh()


def test_process_refreshed_access_token_with_exp() -> None:
    common = _build_common()
    exp = int((datetime.now(timezone.utc) + timedelta(days=14)).timestamp())
    token = jwt.encode({"exp": exp}, "secret", algorithm="HS256")
    result = common._process_refreshed_access_token(token)
    assert result.access_token == token
    # Stored as ISO format with trailing Z; parsed_expiration_time should
    # round-trip back to the same UTC timestamp.
    assert int(result.parsed_expiration_time().timestamp()) == exp


def test_process_refreshed_access_token_missing_exp_warns(
    caplog: pytest.LogCaptureFixture,
) -> None:
    common = _build_common()
    token = jwt.encode({"sub": "user"}, "secret", algorithm="HS256")
    caplog.set_level(logging.WARNING, logger="yalexs.authenticator_common")
    result = common._process_refreshed_access_token(token)
    assert any(
        r.name == "yalexs.authenticator_common"
        and r.levelno == logging.WARNING
        and "exp" in r.getMessage()
        for r in caplog.records
    ), f"expected exp warning, got: {caplog.records}"
    # Returned the existing authentication unchanged.
    assert result.access_token == "old_token"
